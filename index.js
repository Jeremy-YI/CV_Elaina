// page scroll
const sections = document.querySelector(".page");
function scrollActive() {
  const scrollY = window.pageYOffset;
  sections.forEach(function (section) {
    const sectionHeight = section.offsetHeight;
    const sectionTop = section.offsetTop;
    sectionId = section.getAttribute("id");

    if (scrollY > sectionTop && scrollY <= sectionTop + sectionHeight) {
      document
        .querySelector(".navbar a[href*=" + sectionId + "]")
        .classList.add("active-link");
    } else {
      document
        .querySelector(".navbar a[href*=" + sectionId + "]")
        .classList.remove("active-link");
    }
  });
}
window.addEventListener("scroll", scrollActive);

// display images by category
const sectionCenter = document.querySelector(".section-center");
const filterBtn = document.querySelectorAll(".filter-btn");

// load items
window.addEventListener("DOMContentLoaded", function () {
  displayItems(images);
});

// filter items
filterBtn.forEach(function (btn) {
  btn.addEventListener("click", function (e) {
    const category = e.currentTarget.dataset.id;
    const btnCategory = images.filter(function (item) {
      if (item.category === category) {
        return item;
      }
    });
    if (category === "all") {
      displayItems(images);
    } else {
      displayItems(btnCategory);
    }
  });
});

function displayItems(items) {
  let displayPhoto = items.map(function (item) {
    return `
      <article class="img-item">
        <img
          src=${item.image}
          class="photo"
          alt="photo item"
        />
        <div class="img-item-info">
          <h6>Create in: ${item.time}</h6>
        </div>
      </article>
      `;
  });
  displayPhoto = displayPhoto.join("");
  sectionCenter.innerHTML = displayPhoto;
}
